print(l1)

#Insert function -
l1 = [1, 8, 9, 24, 45, 15]
print(l1)
l1.insert(1,6)  #(updated list) # inserts 6 at the index 1
print(l1) 

#Pop Function
l1 = [1, 8, 9, 24, 45, 15]
print(l1)
l1.pop(2)   # removes element at index 4
print(l1)

#Remove Function
