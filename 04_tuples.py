# Tuples = A tuples is a immutable(cannot change) data type in python
# Once defined a tuple elements cannot be altered or manipulated.


# Creating a tuple using()
t = (1,2,4,5)
t1 =()  # Empty tuple
t1 = (1) # Wrong way to declare  a tuple with single element
t1 = (t1,) # Tuple with single element
print(t1)


#Printing the elements of tuple
print(t[0])

#Cannot  update the values of a tuple
t[0] = 34
